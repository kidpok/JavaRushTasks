/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {

        int a = 0;
        while (a<5){
            int b = 0;
           while (b<9){
                System.out.print("Q");
               b++;
            }
            System.out.println("Q");
            a++;
        }

    }
}